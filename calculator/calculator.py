class Calculator:

    def sum(self, first_number: int, second_number: int) -> int:
        return first_number + second_number

    def subtraction(self, first_number: int, second_number: int) -> int:
        return first_number - second_number

    def multiplication(self, first_number: int, second_number: int) -> int:
        return first_number * second_number

    def division(self, first_number: int, second_number: int) -> float:
        return first_number / second_number
